package edu.hzcc.webdemo.tables;

public class TPerson {
	private long id;
	private String idcard;
	private String name;
	private String password;
	private String sex;
	private String xianluhao;
	private String chufashijian;
	private String chehao;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getIdcard() {
		return idcard;
	}
	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getXianluhao() {
		return xianluhao;
	}
	public void setXianluhao(String xianluhao) {
		this.xianluhao = xianluhao;
	}
	public String getChufashijian() {
		return chufashijian;
	}
	public void setChufashijian(String chufashijian) {
		this.chufashijian = chufashijian;
	}
	public String getChehao() {
		return chehao;
	}
	public void setChehao(String chehao) {
		this.chehao = chehao;
	}
	
}
